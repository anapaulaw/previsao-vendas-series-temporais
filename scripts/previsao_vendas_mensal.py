import streamlit as st


st.title("Previsão de Vendas Mensais")

uploaded_file = st.file_uploader("VendasMensais.csv", type="csv")

if uploaded_file:
    df = pd.read_csv(uploaded_file)
    df['Period'] = pd.to_datetime(df['Period'], format='%d.%m.%Y')
    df.set_index('Period', inplace=True)
    df = df.dropna(subset=['Revenue'])

    st.line_chart(df['Revenue'])

    meses = st.slider("Meses a prever", 1, 24, 12)

    if st.button("Gerar Previsão"):
        modelo_auto = auto_arima(df['Revenue'], seasonal=True, m=12, trace=False)
        model = SARIMAX(df['Revenue'],
                        order=modelo_auto.order,
                        seasonal_order=modelo_auto.seasonal_order)
        model_fit = model.fit(disp=False)
        pred = model_fit.get_forecast(steps=meses)
        pred_ci = pred.conf_int()
        start_date = df.index.max() + pd.DateOffset(months=1)
        forecast_index = pd.date_range(start=start_date, periods=meses, freq='MS')
        forecast = pred.predicted_mean
        forecast.index = forecast_index
        pred_ci.index = forecast_index

        fig, ax = plt.subplots(figsize=(12, 4))
        ax.plot(df['Revenue'], label='Histórico')
        ax.plot(forecast, label='Previsão', color='orange')
        ax.fill_between(pred_ci.index,
                        pred_ci.iloc[:, 0],
                        pred_ci.iloc[:, 1], color='pink', alpha=0.3)
